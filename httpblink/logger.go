package httpblink
import (
	"net/http"
	"time"
	"log"
)


func logger(inner http.Handler, name string) http.Handler{
	return http.HandlerFunc(
		func(w http.ResponseWriter, r *http.Request){
			start := time.Now();
			w.Header().Set("Content-Type", "application/json; charset=UTF-8")
			inner.ServeHTTP(w,r)
			log.Printf(
				"%s\t%s\t%s\t%s",
				r.Method,
				r.RequestURI,
				name,
				time.Since(start),
			)
		})
}
